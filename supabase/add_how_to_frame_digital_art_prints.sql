<h3>The Power of Matting (Mounting)</h3>
<p>When selecting a frame, you have two choices:</p>
<ul>
  <li><strong>Frame-to-Edge</strong>: The frame is the exact size of your print. (e.g., an A3 print in an A3 frame). This looks modern, clean, and minimalist.</li>
