<h2>3. DIY Framing Tips: Step-by-Step Assembly</h2>

<p>If you bought readymade frames online and want to assemble them at home, follow these steps to keep your prints clean and aligned:</p>

<ol>
  <li><strong>Clean Your Workspace</strong>: Wipe down your table. Dust is the archenemy of framing. One tiny hair or dust particle trapped under the acrylic will drive you crazy once it is on the wall.</li>
  <li><strong>Clean the Acrylic/Glass</strong>: Use a microfiber cloth and glass cleaner to clean both sides of the glass or acrylic insert. Let it dry completely.</li>
  <li><strong>Align the Print</strong>: If using a mat, center your print behind the mat window. Use acid-free painter''s tape (or standard paper tape) to secure only the top edge of the print to the mat. <em>Do not tape all four sides</em>, as changes in humidity will cause the paper to warp if it cannot expand slightly.</li>
  <li><strong>Secure the Backing</strong>: Place the print/mat sandwich inside the frame, put the backing board on, and lock the metal tabs.</li>
</ol>

